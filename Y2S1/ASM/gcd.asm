MODEL SMALL
.STACK 100H
.DATA
NUM1 DB 90
NUM2 DB 25
.CODE
MAIN PROC
 MOV AX, @DATA
 MOV DS, AX
 MOV AX, 0
 MOV BX, 0
 MOV AL, NUM1
 MOV BL, NUM2
GCD:
 MOV CX, 0
 CMP BL, 0
 JE PRINT
 DIV BL
 MOV CL, AH
 MOV CH, BL
 MOV BL, CL
 MOV AX, 0
 MOV AL, CH
 JMP GCD
PRINT:
 MOV DX, 0
 MOV DL, AL
 ADD DL, 48
 MOV AH, 02H
 INT 21H
 MOV AH, 4CH
 INT 21H
MAIN ENDP
END MAIN
