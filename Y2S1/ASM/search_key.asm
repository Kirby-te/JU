.MODEL SMALL
.STACK 100H
.DATA
ARR1 DB 1, 4, 2, 5, 6, 7
VAR1 DB "ENTER KEY", 10, 13, "$"
VAR2 DB "FOUND", 10, 13, "$"
VAR3 DB "NOT FOUND", 10, 13, "$"
.CODE
MAIN PROC
 MOV AX, @DATA
 MOV DS, AX
 MOV DX, OFFSET VAR1
 MOV AH, 09H
 INT 21H
 MOV AH, 01H
 INT 21H
 SUB AL, 48
 MOV SI, OFFSET ARR1
 MOV CX, 6
L1:
 MOV BL, [SI]
 CMP AL, BL
 JE PRINT
 INC SI
 LOOP L1
 MOV DX, 10
 MOV AH, 02H
 INT 21H
 MOV DX, 13
 MOV AH, 02H
 INT 21H
 MOV DX, OFFSET VAR3
 MOV AH, 09H
 INT 21H
 MOV AH, 4CH
 INT 21H
PRINT:
 MOV DX, 10
 MOV AH, 02H
 INT 21H
 MOV DX, 13
 MOV AH, 02H
 INT 21H
 MOV DX, OFFSET VAR2
 MOV AH, 09H
 INT 21H
 MOV BX, 6
 SUB BX, CX
 MOV DX, BX
 ADD DX, 30H
 MOV AH, 02H
 INT 21H
 MOV AH, 4CH
 INT 21H
MAIN ENDP
END MAIN
